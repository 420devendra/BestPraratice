package com.sebone.java.productsearch.model;
/**Class : ProductData
 * @author DevendraMalve
 * Date: 24-03-2022
 * Objective : Taking Product Details as Input and Giving the Output
 */
public class ProductData {
	int productId;
	String productName;
	int productPrice;
	String productBrandName;
	String productStatus;
	String productDesCription;
	int productRating;
	int productDiscount;
	/**
	 * @return the productId
	 */
	public int getProductId() {
		return productId;
	}
	/**
	 * @param productId the productId to set
	 */
	public void setProductId(int productId) {
		this.productId = productId;
	}
	/**
	 * @return the productName
	 */
	public String getProductName() {
		return productName;
	}
	/**
	 * @param productName the productName to set
	 */
	public void setProductName(String productName) {
		this.productName = productName;
	}
	/**
	 * @return the productPrice
	 */
	public int getProductPrice() {
		return productPrice;
	}
	/**
	 * @param productPrice the productPrice to set
	 */
	public void setProductPrice(int productPrice) {
		this.productPrice = productPrice;
	}
	/**
	 * @return the productBrandName
	 */
	public String getProductBrandName() {
		return productBrandName;
	}
	/**
	 * @param productBrandName the productBrandName to set
	 */
	public void setProductBrandName(String productBrandName) {
		this.productBrandName = productBrandName;
	}
	/**
	 * @return the productStatus
	 */
	public String getProductStatus() {
		return productStatus;
	}
	/**
	 * @param productStatus the productStatus to set
	 */
	public void setProductStatus(String productStatus) {
		this.productStatus = productStatus;
	}
	/**
	 * @return the productDesCription
	 */
	public String getProductDesCription() {
		return productDesCription;
	}
	/**
	 * @param productDesCription the productDesCription to set
	 */
	public void setProductDesCription(String productDesCription) {
		this.productDesCription = productDesCription;
	}
	/**
	 * @return the productRating
	 */
	public int getProductRating() {
		return productRating;
	}
	/**
	 * @param productRating the productRating to set
	 */
	public void setProductRating(int productRating) {
		this.productRating = productRating;
	}
	/**
	 * @return the productDiscount
	 */
	public int getProductDiscount() {
		return productDiscount;
	}
	/**
	 * @param productDiscount the productDiscount to set
	 */
	public void setProductDiscount(int productDiscount) {
		this.productDiscount = productDiscount;
	}
	
}
